            <h2 class="text-danger mt-3">RUET 2024 Exam Pattern</h2>
            <p dir="ltr">RUET 2024 Exam will be offline pen – paper-based exam to be conducted in 2 hours duration for UG and PG program and 3 hours for the Ph.D. program. The question paper will be in 3 parts. First 2 parts are common for all applicants and 1 part is related to the applied study programs. Sections are named as –</p>
            <p>a) General English</p>
            <p>b) General Knowledge and Numerical Ability</p>
            <p>c) Subject Ability</p>
            <p>Duration of 2 hours provides 1 minute time for 1 question. Objective type multiple choice questions are divided into <strong>3 sections</strong> consisting of 20 questions in each of the first 2 sections and 80 questions in the section. RUET 2024&nbsp;exam is of <strong>total 120 marks</strong>; <strong>each question carries 1 mark for right answer and negative marking is not applicable</strong>.</p>
