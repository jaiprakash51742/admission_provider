               <h3> MBBS Admission Process in India 2024</h3>
               <p class="text-justify">Admission to the MBBS course is granted on the basis of candidates’ performance in the national level entrance exam, NEET. Check the MBBS admission process in India below. </p>

               <h3> MBBS entrance exams</h3>
               <p class="text-justify"> As aforementioned, for MBBS admission there is only one all India medical entrance exam - NEET. NTA conducts the medical entrance test based on the NEET-UG syllabus which includes topics from class 11 and 12 CBSE curriculum. As per the NEET exam pattern, MBBS admission aspirants need to answer 180 multiple choice questions out of 200 questions in 3 hours.</p>
               <p>The NEET 2024 question paper is divided into three sections - Physics, Chemistry, and Biology (Botany + Zoology). The NEET biology section carries the highest weightage with 90 questions that need to be answered, which fetch 360 marks. The remaining two sections of the NEET paper have equal weightage, that is 45 questions and 180 marks each.</p>

               <h3> What after qualifying NEET exam?</h3>
               <p class="text-justify"> Once candidates have qualified for the NEET exam, they will be eligible to participate in the central and state-level and AIQ counselling process conducted for MBBS admission. NTA will prepare the list of NEET-UG-qualified candidates. Shortlisted students will be eligible for NEET counselling. To participate in the counselling and seat allotment process, students will have to register at the Medical Counselling Committee (MCC) portal.</p>
               <p>MCC will conduct the NEET counselling for 15% of All India Quota, deemed/central universities/AFMS, and ESIC institutes. During counselling for MBBS admission, students will have to register using their personal and NEET-related details, pay the counselling fee, and fill their preferences of the course and the colleges they wish to study into. Based on the choices filled by candidates, NEET All India Rank, reservation, and seat matrix, MCC will conduct the NEET UG seat allotment. Candidates who will be offered seats during seat allotment of MBBS admission will have to report to the allotted institute.</p>


               <h3> State-wise MBBS admissions in India</h3>
               <p class="text-justify"> Admission to MBBS to the remaining 85% state quota seats in government colleges and all seats in private institutes will be conducted by respective state authorities. The counselling for MBBS admission can be conducted in offline or online mode. During state counselling of MBBS admission, candidates will have to register, fill choices of course and colleges, and undergo document verification. In addition to state quota seats, MBBS admission to 100% seats in private colleges in the respective state will be conducted by state authorities. Check the state-wise MBBS admission process below.</p>

