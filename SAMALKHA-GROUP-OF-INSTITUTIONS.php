                                       <h3 class="ramiya">Admission Procedure & Eligibility</h3>
                                     <ul class="blog-post-list">
                                         <li>Prospectus and application forms are available from our registered offices on payment of Rs. 850/- online and 850/- offline in cash or demand draft.</li>
                                         <li>The Demand Draft should be drawn in favour of “Nav Nirman Sewa Samiti” payable at Delhi.</li>
                                     </ul>
                                     <h4 class="subhead">Master Of Technology (M.Tech)</h4>
                                     <ul class="blog-post-list">
                                         <li>Bachelor’s Degree or equivalent in the relevant field.</li>
                                         <li>Obtained at least 50% marks (47.75% in case of candidates belonging to reserved category) at the qualifying examination.</li>
                                     </ul>
                                     <h4 class="subhead">Bachelor Of Technology (B.Tech)</h4>
                                     <ul class="blog-post-list">
                                         <li>Passed 10+2 examination with Physics and Mathematics as compulsory subjects along with one of the Chemistry/Biotechnology/Biology/Technical vocational subjects.</li>
                                         <li>Obtained at least 45% marks (42.75% in case of candidates belonging to reserved category) in the above subjects taken together.</li>
                                     </ul>
                                     <h4 class="subhead">Bachelor Of Technology - Lateral Entry (B.Tech-LEET)</h4>
                                     <ul class="blog-post-list">
                                         <li>Passed diploma examination from an AICTE approved institution, with at least 45% marks (42.75% in case of candidates belonging to reserved category) in appropriate branch of engineering/Technology.</li>
                                         <li>Passed B.Sc. degree from a recognized University as defined by UGC, with at least 45% marks (42.75% in case of candidates belonging to reserved category) and passed XII standard with mathematics as a subject.</li>
                                         <li>Provided that in case of students belonging to B.Sc. Stream, shall clear the subjects of Engineering Graphics/Engineering Drawing and Engineering Mechanics of the first year engineering program along with the second year subjects.</li>
                                         <li>Provided further that, the students belonging to B.Sc. stream shall be considered only after filling the supernumerary seats in this category with students belonging to the Diploma stream.</li>
                                         <li>Provided further that students, who have passed diploma in Engineering & technology from an AICTE approved institution or B.Sc. Degree from a recognized University as defined by UGC, shall also be eligible for admission to the first year Engineering Degree courses subject to vacancies in the first year class in case the vacancies for lateral entry are exhausted.</li>
                                     </ul>
                                     <h4 class="subhead">DIPLOMA IN ENGINEERING</h4>
                                     <p>The minimum qualification required for admission into a diploma course is passing of the Secondary School Leaving Certificate (SSLC)/tenth standard/equivalent examination, with at least 35 percent in science and maths subjects.</p>
                                     <h4>DIPLOMA (LEET)</h4>
                                     <p>All those candidates who have passed ITI at least with two year (who passed 10th level school leaving examination before admission to ITI courses)</p>
                                     <p class="text-center">or</p>
                                     <p>10+2 with Physics, Chemistry & Maths (PCM)</p>
                                     <p class="text-center">or</p>
                                     <p>10+2 Vocational would be eligible for consideration for lateral entry in Diploma Engineering Courses.</p>
                                     <h4>Master Of Business Administration (MBA)</h4>
                                     <ul class="blog-post-list">
                                     <li>Recognized Bachelor’s Degree of minimum 3 years duration.</li>
                                     <li>Obtained at least 50% marks (47.75% in case of candidates belonging to reserved category) at the qualifying examination.</li>
                                 </ul>
