               <h2 style="text-align: justify;" class="text-danger text-center">JEE Main 2024 Syllabus </h2>
               <p style="text-align: justify;">Complete JEE Main Syllabus 2024 can be checked by the candidates online from the website. The topics in the syllabus are mostly from the 11th & 12th level syllabus. This year, syllabus has been revised. Fewer topics have been reduced from each subjects.</p>
               <p style="text-align: justify;">For catering the decision of various Boards in the country related to syllabus reduction, National Testing Agency provides a choice in one section of each subject of paper 1 & paper 2A/B (part 1). The total number of questions attempted remains same as (Physics-25; chemistry-25; Mathematics-25), wherever relevant.</p>
               <h4 class="text-danger">For Paper 1 (B.E/ B.Tech):</h4>
               <p style="text-align: justify;">The questions will be asked from various topics covered in Physics, Chemistry &amp; Mathematics subjects:</p>
               <p style="text-align: justify;"><strong>Physics</strong> – Laws of Motion, Kinematics, Work, Energy &amp; Power, Gravitation, Rotational Motion, Physics &amp; Measurement, Properties of Solids &amp; Liquids, Thermodynamics, Electrostatics, Magnetic Effects of Current &amp; Magnetism, Current Electricity, Optics, Electromagnetic Waves, Dual Nature of Matter &amp; Radiation, Electronic Devices, Atoms &amp; Nuclei etc.</p>
               <p style="text-align: justify;"><strong>Chemistry</strong> – Physical Chemistry- Atomic structure, chemical thermodynamics, chemical kinetics, some basic concepts in chemistry, equilibrium, states of matter etc.</p>
               <p style="text-align: justify;"><strong>Organic Chemistry</strong>– Purification &amp; characterization of organic compounds, some basics principles of organic chemistry, chemistry in everyday life, Organic compounds containing nitrogen, organic compounds containing halogens etc.</p>
               <p style="text-align: justify;"><strong>Inorganic Chemistry</strong>– Block elements (alkali &amp; alkaline earth metals), environmental chemistry, d &amp; f block elements, Hydrogen, classifications of elements &amp; periodicity in properties, block element group 13 to group 18 elements, etc.</p>
               <p style="text-align: justify;"><strong>Mathematics</strong> – Sets, Relations &amp; Functions, Matrices &amp; Determinants, Permutations &amp; Combinations, Complex Numbers &amp; &nbsp;Sequences &amp; Series, Differential Equations, Integral Calculus, Limit, Continuity &amp; Differentiability, Three Dimensional Geometry, Vector Algebra,Trigonometry etc.</p>
               <h4 class="text-danger">For Paper 2 A (B.Arch):</h4>
               <p style="text-align: justify;"><strong>Part I:</strong> The topics in Part 1 of Paper 2A will contain questions from <strong>Mathematics</strong> subject. The topics are similar to the ones as mentioned above in Paper 1 of Mathematics.</p>
               <p style="text-align: justify;"><strong>Part II:</strong> An <strong>aptitude test</strong> will be conducted in this part. Questions will be asked from topics -Awareness of person, places, texture &amp; objects related to architecture &amp; building, visualizing 3D objects from 2D drawings, buildings &amp;materials, analytical reasoning, etc.</p>
               <p style="text-align: justify;"><strong>Part III:</strong> This part covers the <strong>drawing test</strong> in which candidates are asked to sketch scenes &amp; activities from memory of urbanscape (festivals, public space, street scenes, market, monuments, recreational spaces, etc). rural life &amp; landscape (riverfronts, Jungle, Gardens, trees, Plants, etc.). This part is conducted in drawing sheet and students are advised to bring pencils, geometry box, crasets, pencils, crayons, etc.</p>
               <h4 class="text-danger">For Paper 2 B (B.Plan):</h4>
               <p style="text-align: justify;"><strong>Part I:</strong> The topics in Part 1 of Paper 2B will contain questions from <strong>Mathematics</strong> subject. The topics are similar to the ones as mentioned above in Paper 1 &amp; 2A of Mathematics.</p>
               <p style="text-align: justify;"><strong>Part II: </strong>An <strong>aptitude test</strong> will be conducted in this part. Questions will be asked from topics -Awareness of person, places, texture &amp; objects related to architecture &amp; building, visualizing 3D objects from 2D drawings, buildings &amp;materials, analytical reasoning, etc.</p>
               <p style="text-align: justify;"><strong>Part III:</strong> This part will contain questions related to various topics of planning. The part will cover various questions from topics – <strong>General Awareness, Social sciences &amp; Thinking skills.</strong></p>
