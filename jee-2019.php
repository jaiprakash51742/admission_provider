                            <h4 class="subhead">Eligibility Criteria for JEE Main 2020</span></h4>
                            <ol style="list-style-type:disc;">
                                <li class="text-justify mt-3">JEE Main Eligibility Criteria for Age: All candidates (General Category) born on or after 1st of October 1994 are eligible for JEE Mains 2020. For SC, ST and PwD candidates, the age limit has been relaxed and should have born on or after 1st of October 1989 for being eligible.</li>
                                <li>JEE Main Eligibility Criteria for total Attempts: The students can avail a maximum of three consecutive attempts for JEE Mains and two consecutive attempts for JEE Advanced. The first attempt is taken soon after 12th board exams followed by the other attempts in consecutive years.</li>

                            </ol>
                            <h4 class="subhead">JEE Main Eligibility Marks</h4>
                            <p>There are no minimum marks required for JEE Main in class 12th board exams. However, the appearing candidates have to secure at least the passing marks in their respective qualifying examinations.</p>
                            <ol style="list-style-type:disc;">
                                <li>For general category candidates: The students must have secured a minimum of<span class="comname"> 75% marks </span>in their class 12th board exams or they must rank among the top 20 percentile in their qualifying examination for getting admissions in IITs, NITs, IIITs and other CFTIs.</li>
                                <li>For reserved category candidates: A minimum of 65% marks are required for getting admissions in different undergraduate courses offered at IITs, NITs, IIITs and other CFTIs.</li>
                            </ol>
                            <h4 class="subhead"><span style="color: #cd1000; font-size: 18px;">JEE Main Eligibility Criteria for Subjects and Year of Passing</span></h4>
                            <ol style="list-style-type:disc;">
                                <li>Eligibility Criteria for Subjects in the Qualifying Exam: It is mandatory for all students appearing for JEE Mains that they must have pursued at<span class="comname"> least 5 subjects </span>with Physics and Maths as their core subjects in their qualifying examinations. Students who have studied less than 5 subjects in their qualifying exam are not eligible for JEE Main.</li>
                                <li>Eligibility Criteria for Year of passing: Students who have passed their 12th class examination or other equivalent qualifying examination in 2017 or 2018 are eligible for appearing in JEE Mains 2020. Students currently appearing for their class 12th examination or other equivalent qualifying examination are also eligible, provided their result should be declared before 15th of June 2018.</li>
                            </ol>
                            <h4 class="subhead">JEE Main Eligibility Criteria for Droppers</span></h4>
                            <p>As per the eligibility criteria for JEE Main 2020, the students are allowed to appear for the Mains exam in three consecutive years. The 1st attempt is taken soon after the board exams and the remaining two attempts can be taken in the upcoming consecutive years. Here we have provided the detailed information on JEE Main eligibility criteria for droppers. The students are advised to read the below-mentioned eligibility criteria to avoid rejection of the Application forms.</p>
                            <ol style="list-style-type:disc;">
                                <li>The applicant (General Category) should have born on or after 1st of October 1994 and1st of October 1989 for ST, SC, and PwD candidates.</li>
                                <li>Must have completed class 12th board exams or other equivalent exams in 2018 or 2017.</li>
                                <li>Minimum of Five subjects opted in class 12 board exams with Maths and Physics as compulsory subjects.</li>
                                <li>The candidates aspiring for IIITs, NITs, and GFTIs must have secured 75% or more (65% for For ST, SC, and Pwd Candidates) in class 12th board exams (other equivalent exams) or within the top 20 percentile of their respective boards.</li>
                            </ol>
                            <p>If the applicant fulfils all of the above mentioned JEE Main eligibility criteria for droppers except the eligibility criteria for admissions to IIITs. NITs, and GFTIs, then the candidate can appear for the improvement exam.</p>
                            <ol style="list-style-type:disc;">
                                <li>If the improvement exam is taken for all the subjects the percentage marks secured in the improvement exam will be considered in the final results.</li>
                                <li>If the improvement exam is taken for particular subjects (not all) the percentage marks scored in the original passing year will be considered.</li>
                            </ol>
                            <p>JEE Main eligibility criteria for droppers and class 12th candidates has to be fulfilled by all the engineering aspirants. JEE Main is also an eligibility test for students aspiring for JEE Advanced. The top 2,24,000 candidates qualifying Mains will be allowed to appear for the Advanced exam.</p>
