            <h4 class="subhead">JIPMER MBBS Admission 2020:</h4>
            <p>JIPMER offers MBBS program for the duration of 4 years 6 months followed by the one-year compulsory internship with a total intake of 200 seats. Candidates must have passed 10+2 with Physics, Chemistry and Biology/ Bio-Technology with minimum 60% aggregate of marks. Admission to the MBBS course is done on the basis of JIPMER entrance examination. The application process, as well as the entrance examination, is conducted through online mode. Seats are reserved for OBC, ST, SC and NRI/ OCI category candidates as well as OPH candidates.<br>
               JIPMER application form can be filled through the official website within the stipulated time. The application fee for JIPMER is INR 1500 for General and OBC candidates whereas it is INR 1200 for SC and ST category students. JIPMER application form filling consists of 5 steps which include entering the candidate’s credentials, uploading signature and photo, submission of application form, payment of fees and generation of the final application for printout. OPH and P-OPH candidates are exempted from any application fee.
            </p>
