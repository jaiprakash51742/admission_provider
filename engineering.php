            <h4>What is Engineers?</h4>
            <p class="text-justify"><span class="font-weight-bold">Engineers,</span> is the partitions of engineering, are professionals who invent, design, analyze, build and test machines, complex systems, structures, gadgets and materials to fulfill functional objectives and requirements while considering the limitations imposed by practicality, regulation, safety and cost. The foundational qualifications of an engineer typically include a four-year bachelor's degree in an engineering discipline, a master's degree in an engineering discipline plus four to six years of professional practice.</p>

            <h4>The field of Engineering in spread in vast areas:-</h4>
            <p class="text-justify"><span class="font-weight-bold">Mechanical Engineering</span> involves design, manufacturing and maintenance of machinery, equipment and components as well as control systems and instruments for monitoring their status and performance. This includes vehicles, construction and farm machinery, industrial installations and a wide variety of tools and device.</p>
            <p class="text-justify"><span class="font-weight-bold">Electrical Engineering </span>involves design, testing, manufacturing, construction, control, monitoring and inspection of electrical and electronic devices, machinery and systems. These systems vary in scale from microscopic circuits to national power generation and transmission system.</p>
            <p>Civil Engineering involves design, construction, maintenance and inspection of large infrastructure projects such as highways, railroads, bridges, tunnels, dams and airport.</p>
            <p class="text-justify"><span class="font-weight-bold">Aerospace Engineering</span> involves design, manufacturing and testing of aircraft and spacecraft as well as parts and components such as airframes, power plants, control and guidance systems, electrical and electronic systems, and communication and navigation system.</p>
            <p class="text-justify">Nuclear Engineering involves design, manufacturing, construction, operation and testing of equipment, systems and processes involving the production, control and detection of nuclear radiation. Nuclear engineering also includes monitoring and protecting humans from the potentially harmful effects of radiations.</p>
            <p class="text-justify"><span class="font-weight-bold">Biomedical Engineering </span>is the practice of designing systems, equipment and devices for use in the practice of medicine. It also involves working closely with medical practitioners, including doctors, nurses, technicians, therapists and researchers, in order to determine, understand and meet their requirements for systems, equipment and device.</p>
            <p class="text-justify"><span class="font-weight-bold">Chemical Engineering</span> is the practice of designing equipment, systems and processes for refining raw materials and for mixing, compounding and processing chemicals to make valuable product.</p>
            <p class="text-justify"><span class="font-weight-bold">Computer Engineering</span> is the practice of designing computer hardware components, computer systems, networks and computer software.</p>
            <p class="text-justify"><span class="font-weight-bold">Industrial Engineering</span> is the practice of designing and optimizing facilities, equipment, systems and processes for manufacturing, material processing, and any number of other work environments.</p>
            <p class="text-justify"><span class="font-weight-bold">Environmental Engineering</span> is the practice of preventing, reducing and eliminating sources of pollution that affect air, water and land. It also involves detecting and measuring pollution levels, determining sources of pollution, cleaning up and rehabilitating polluted sites and ensuring compliance with local, state and federal regulations.</p>

