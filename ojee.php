<h2 class="text-danger"><strong>OJEE 2023 Syllabus</strong></h2>
<p>The OJEE 2023 offers admission to large number of courses and different entrance exam are conducted for admission to different courses. Here is the syllabus for some courses given below.</p>
<p><strong>MCA</strong>: Mathematics (Calculus, Vectors, Sequence and Series, Probability and Statistics, etc.), Computer Awareness (Introduction to Computer, C language, Computer Arithmetic, etc.)</p>
<p><strong>Lateral Entry MCA</strong>: Introduction to Computer, Fundamentals of Computer Organization and Networking, Computer Arithmetic, Introduction to Operating systems, C Language, Logical reasoning and verbal abilities, etc.</p>
<p><strong>B.Sc. Lateral Entry</strong>: Electrostatics, Electrochemistry, Atomic Structure, Periodic Properties, Chemical Bonding, Mechanics, Oscillations, Motion of charged particles in electric and magnetic fields, Mathematics (Determinants and matrices, Trigonometry, Logic, Algebra of sets, Number system), etc.</p>
