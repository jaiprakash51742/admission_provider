<h2 class="text-danger mt-2"><strong>OJEE Eligibility Criteria</strong></h2>
<p>Candidates must check the following<strong>eligibility criteria</strong>for OJEE 2023. The eligibility criteria for OJEE are different for different courses.</p>
<ul>
<li><strong>Nationality:&nbsp;</strong>The candidate must be an&nbsp;<strong>Indian citizen.</strong></li>
</ul>
<h3 class="text-danger mt-2"><strong>For B. Pharm:</strong></h3>
<ul>
<li><strong>Qualification:</strong>&nbsp;Candidates must qualify&nbsp;<strong>class 12</strong><strong>th</strong>&nbsp;from any recognized board or university.</li>
<li><strong>Subjects:</strong>&nbsp;The candidates must qualify with Physics and Chemistry as compulsory subjects along with Mathematics/ Biology.</li>
</ul>
<h3 class="text-danger mt-2"><strong>For MBA,&nbsp;</strong><strong>M.Tech/ M.Arch/ M.Plan:</strong></h3>
<ul>
<li><strong>Qualification:</strong>&nbsp;Candidate must hold the&nbsp;<strong>Bachelor&rsquo;s degree</strong>&nbsp;from any recognized university/ institute.</li>
<li><strong>Marks:</strong>&nbsp;Candidate must score minimum of&nbsp;<strong>50% marks (45% in case of SC/ST)</strong>&nbsp;in qualifying exam.</li>
</ul>
<h3 class="text-danger mt-2"><strong>For MCA:</strong></h3>
<ul>
<li><strong>Qualification:</strong>&nbsp;Candidate must pass&nbsp;<strong>bachelor&rsquo;s degree</strong>&nbsp;in computer science/ BCA or equivalent degree or</li>
<li>Candidate must pass B.Sc./ B.Com/ BA with Mathematics at 12th&nbsp;class or at graduation level.</li>
<li><strong>Marks:</strong>&nbsp;The minimum mark candidate must obtain will be&nbsp;<strong>50% marks (45%</strong>&nbsp;for reserved category) in qualifying exam.</li>
</ul>
<h3 class="text-danger mt-2"><strong>For M.Pharm:</strong></h3>
<ul>
<li><strong>Qualification:</strong>&nbsp;Candidate must pass&nbsp;<strong>Bachelor degree</strong>&nbsp;in Pharmacy.</li>
<li><strong>Marks:</strong>&nbsp;Candidate must obtain at least&nbsp;<strong>55% marks (50%</strong>&nbsp;for reserved category) in qualifying examination.</li>
</ul>
<h3 class="text-danger mt-2"><strong>For B.TECH (LE):</strong></h3>
<ul>
<li><strong>Qualification:</strong>&nbsp;Candidate must pass minimum&nbsp;<strong>3 years or 2 years diploma exam</strong>&nbsp;from any recognized college/ institute.&nbsp;</li>
<li><strong>Subject:</strong>&nbsp;Candidate must pass in any branch such as Engineering and Technology to be eligible.</li>
<li><strong>Marks:</strong>&nbsp;The minimum marks candidate must obtain will be&nbsp;<strong>45% marks (40%</strong>&nbsp;marks for reserved category) to be eligible for exam.</li>
</ul>
<h3 class="text-danger mt-2"><strong>For B.PHARM (LE):</strong></h3>
<ul>
<li><strong>Qualification:</strong>&nbsp;Candidate must pass&nbsp;<strong>Diploma exam</strong>&nbsp;in a Programme from any recognized institute/ college.</li>
<li><strong>Marks:</strong>&nbsp;Candidate must score at least&nbsp;<strong>45% (40% marks</strong>&nbsp;for reserved category) in any qualifying examination.</li>
</ul>
