                  <p><strong>Verbal Ability and Reading Comprehension (VARC)</strong></p>
                  <ul>
                     <li>The VARC section is divided into two main parts; reading comprehension and verbal ability. A major section of the VARC is reading comprehension which includes paragraphs of varying lengths with MCQ and TITA questions. The verbal ability section comprises parajumbles, odd sentences etc.</li>
                  </ul>
                  <p><strong>Data Interpretation and Logical Reasoning (DILR)</strong></p>
                  <ul>
                     <li>Data Interpretation consists of sets of questions based on various charts, graphs and other forms of data presentation. The data for the answers is to be directly extracted from the given charts. The logical reasoning section also consists of sets of questions based on seating arrangements, blood relations, puzzles etc.</li>
                  </ul>
                  <p><strong>Quantitative Aptitude (QA)</strong></p>
                  <ul>
                     <li>This section is focused on mathematics as the syllabus for QA is entirely based on school maths. Topics included are LCM &amp; HCF, trigonometry, mensuration, probability etc. In recent years the difficulty of this section has been reduced by the IIMs.</li>
                  </ul>
