                                    <h3 class="ramiya">Sinhgad College of Engineering, Vadgaon Admission 2020: Cut Off, Reservation</h3>
                                    <p class="text-justify">Sinhgad College of Engineering (SCOE) is private unaided institution located in Vadgaon, Pune, established in the year 1996. Affiliated to Savitribai Phule Pune University (SPPU), the institute offers BE programs in 8 specializations Mechanical Engineering, Electronics & Telecommunication Engineering, Computer Engineering, Chemical Engineering.</p>
                                    <p>Six engineering departments offer post-graduate degrees which comprise of MBA and M. Tech program. In addition to it, the college also offers a doctoral degree in selected departments.</p>
                                    <ul class="blog-post-list">
                                        <li>For admission to BE, candidate must have cleared 10+2 in science stream with Physics and Mathematics as compulsory subjects</li>
                                        <li>For M.Tech admission, candidates must possess B.E. / B. Tech degree in the relevant or allied discipline.</li>
                                        <li>MBA candidates should have scored at least 50% marks in graduation in any discipline.</li>
                                        <li>Ph.D. candidates are required to have a Master’s degree in the relevant or allied discipline.</li>
                                        <li>Candidates seeking admission to BE program can apply through MHT CET website.</li>
                                        <li>Candidates can apply for M. Tech or MBA programs through DTE website.</li>
                                        <li>Ph.D. candidates can apply directly through the institute website or collect the application form in person.</li>
                                        <li>The mode of application and fee payment for B.E., M. Tech. and MBA courses is online only.</li>
                                        <li>Both online and offline mode of payment are applicable for Ph.D programs.</li>
                                        <li>Candidates applying for M. Tech. and MBA courses are selected through a common counselling process (CAP ).</li>
                                        <li>B.E. candidates are selected on the basis of marks obtained in MHT CET, followed by a state-level counselling session.</li>
                                        <li>Ph.D. candidates are selected on the basis of marks obtained by the candidate in the last qualifying exam.</li>
                                        <li>Reservation is provided as per norms specified by GOI and Maharashtra State Government.</li>
                                    </ul>
