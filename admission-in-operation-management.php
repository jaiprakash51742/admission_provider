                <h4 class="subhead mt-2">MBA Operations Management: Admission Process</h4>
                <p>Most Management Institutes conduct National level Entrance tests for selection of students. Some of these tests are CAT, XAT, MAT etc. The application process generally starts after the month of August and goes on till December for most of the Entrance Tests. The admission process can be explained via the following Steps:</p>
                <li>Application Process</li>
                <li>Entrance Test</li>
                <li>GD/P I, Final Admission List</li>
                <p>These tests are generally Computer-based except for a few like SNAP which is a paper-based test. After the Entrance Test, every Institute shortlists candidates who then appear for Group Discussion and Personal Interview. Some Institutes like <b>IIM Ahmedabad</b>, <b>IIM Bangalore</b>, and <b>IIM Calcutta</b> do not have a group discussion round but conduct grilling sessions of Personal Interview.</p>
