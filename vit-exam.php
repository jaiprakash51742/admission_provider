               <h2 class="text-danger mt-3">VITEEE 2024 Admit Card</h2>
               <p>The VITEEE admit card 2024 will be released by the authority through <strong> online mode</strong> on the website. It will be released in the second week of April 2024. Candidates will be required to provide their <strong>application form number and online test booking password </strong>to download the admit card. The admit card will be generated for those candidates who booked their test slot through online test booking system. Candidates are advised to keep the hardcopy of the admit card safe for the day of examination.</p>
               <p style="text-align:justify;">In order to download the admit card candidate have to enter their application form number and online test booking password. The candidate must carry the admit card to the examination hall in order to appear for the exam.</p>
               <h2 class="text-danger">VITEEE 2024 Result</h2>
               <p>The authority will publish the VITEEE result 2024 through <strong>online mode</strong>  on the website. It will be released in the month of May 2024. The candidate’s rank will be intimated to their registered mobile number. A merit list will be prepared by the authority on the basis of the total marks secured by the candidates in the entrance exam. Applicants will have to enter their details like application number and password to download the result. Keep the hardcopy of the result for the further usage in the admission procedure.</p>
